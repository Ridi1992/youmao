package com.lester.youmao.entity;

import java.util.ArrayList;

public class CheckOrder {
	private ArrayList<OrderList> goods_list;
	private Consignee consignee;
	private ArrayList<ShippingList> shipping_list;
	private ArrayList<PaymentList> payment_list;
	private String your_integral;
	private ArrayList<Coupon> coupons_list;
	
	public ArrayList<OrderList> getGoods_list() {
		return goods_list;
	}
	public void setGoods_list(ArrayList<OrderList> goods_list) {
		this.goods_list = goods_list;
	}
	public Consignee getConsignee() {
		return consignee;
	}
	public void setConsignee(Consignee consignee) {
		this.consignee = consignee;
	}
	public ArrayList<ShippingList> getShipping_list() {
		return shipping_list;
	}
	public void setShipping_list(ArrayList<ShippingList> shipping_list) {
		this.shipping_list = shipping_list;
	}
	public ArrayList<PaymentList> getPayment_list() {
		return payment_list;
	}
	public void setPayment_list(ArrayList<PaymentList> payment_list) {
		this.payment_list = payment_list;
	}
	public String getYour_integral() {
		return your_integral;
	}
	public void setYour_integral(String your_integral) {
		this.your_integral = your_integral;
	}
	public ArrayList<Coupon> getCoupons_list() {
		return coupons_list;
	}
	public void setCoupons_list(ArrayList<Coupon> coupons_list) {
		this.coupons_list = coupons_list;
	}
	
}
